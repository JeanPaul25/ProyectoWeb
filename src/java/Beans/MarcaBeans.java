
package Beans;

public class MarcaBeans {
    
    int IDmarca;
    String nombre;

    public MarcaBeans(int IDmarca, String nombre) {
        this.IDmarca = IDmarca;
        this.nombre = nombre;
    }

    public int getIDmarca() {
        return IDmarca;
    }

    public void setIDmarca(int IDmarca) {
        this.IDmarca = IDmarca;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
    
}
